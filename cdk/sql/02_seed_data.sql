INSERT INTO orders (customer_id, order_date, total_amount, status)
SELECT
  (random() * 49)::int + 1,
  CURRENT_DATE - ((random() * 90)::int || ' days')::interval,
  0,
  (ARRAY['completed','completed','completed','completed','cancelled','pending'])
    [(random() * 5)::int + 1]
FROM generate_series(1, 200) AS s(i);

-- ============================================================
-- 注文明細データ（500件）
-- ============================================================
INSERT INTO order_items (order_id, product_id, quantity, unit_price)
SELECT
  sub.oid,
  sub.pid,
  sub.qty,
  p.price
FROM (
  SELECT
    (random() * 199)::int + 1 AS oid,
    (random() * 29)::int + 1 AS pid,
    (random() * 4)::int + 1 AS qty
  FROM generate_series(1, 500)
) sub
JOIN products p ON p.product_id = sub.pid;

-- ============================================================
-- 注文の合計金額を更新
-- ============================================================
UPDATE orders o
SET total_amount = COALESCE(
  (SELECT SUM(quantity * unit_price)
   FROM order_items oi
   WHERE oi.order_id = o.order_id),
  0
);
